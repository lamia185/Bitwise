#include<bits/stdc++.h>
using namespace std;

int main(){
    int a=5,b=6,x,y,z,k,l,t=0;

    cout<<"Bitwise And"<<endl;
    cout<<(a&b)<<endl;

    cout<<"Bitwise Or"<<endl;
    cout<<(a|b)<<endl;

    cout<<"Bitwise Xor"<<endl;
    cout<<(a^b)<<endl;

    cout<<"Bitwise a's Complement"<<endl;
    cout<<(~a)<<endl;

    cout<<"Bitwise b's Complement"<<endl;
    cout<<(~b)<<endl;

    cout<<"Bitwise Left shift"<<endl;
    cout<<(a<<2)<<endl;

    cout<<"Bitwise Right shift"<<endl;
    cout<<(a>>2)<<endl;

    cout<<"Bitwise And equal"<<endl;
    cout<<(a&=1)<<endl;

    cout<<"Bitwise Or equal"<<endl;
    cout<<(a|=1)<<endl;

    cout<<"Bitwise Left shift equal"<<endl;
    cout<<(t=1<<1)<<endl;

}
